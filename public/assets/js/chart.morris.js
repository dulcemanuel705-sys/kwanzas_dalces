$(document).ready(function(){
  lineChart();
  barChart();
  $(window).resize(function(){
    window.lineChart.redraw();
    window.barChart.redraw();
  });
});

function lineChart(){
  window.lineChart=Morris.Line({
    element:'line-chart',
    data:[
      {y:'2006',a:100,b:90},
      {y:'2007',a:75,b:65},
      {y:'2008',a:50,b:40},
      {y:'2009',a:75,b:65},
      {y:'2010',a:50,b:40},
      {y:'2011',a:75,b:65},
      {y:'2012',a:100,b:90}
    ],
    xkey:'y',
    ykeys:['a','b'],
    labels:['Receitas','Despesas'],
    lineColors:['#0ab5b3','#08a09e'],
    lineWidth:'3px',
    pointSize:4,
    pointFillColors:['#0ab5b3','#08a09e'],
    pointStrokeColors:['#fff','#fff'],
    gridLineColor:'#e0e0e0',
    gridTextColor:'#666',
    gridTextSize:12,
    resize:true,
    redraw:true
  });
}

